
public class Course {
	int courseId;
	String courseName;
	String courseDesc;
	String instructor;
	String price;
	
	public Course() {
		
	}
	
	public Course(int id, String courseName, String courseDesc, String instructor, String price) {
		
		this();
		this.courseId = courseId;
		this.courseName = courseName;
		this.courseDesc = courseDesc;
		this.instructor = instructor;
		this.price = price;
		
	}




}


